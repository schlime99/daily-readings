# ---------------------------------------------------------------------------
# Public entry point
# ---------------------------------------------------------------------------
def build_site(date: datetime.date, reflection: str,
               liturgical_day: str = None) -> str:
    """
    Add today's reflection to the site and regenerate all pages.
    Returns the relative URL path of today's page (e.g. "2026-07-22.html").
    """
    os.makedirs(DOCS_DIR, exist_ok=True)

    record_path = os.path.join(DOCS_DIR, "reflections.json")
    entries = []
    if os.path.exists(record_path):
        try:
            with open(record_path, encoding="utf-8") as f:
                entries = json.load(f)
        except (json.JSONDecodeError, OSError):
            entries = []

    iso = date.isoformat()
    entries = [e for e in entries if e.get("date") != iso]   # replace if re-run
    entries.append({
        "date": iso,
        "liturgical_day": liturgical_day,
        "reflection": reflection,
    })
    entries.sort(key=lambda e: e["date"], reverse=True)

    with open(record_path, "w", encoding="utf-8") as f:
        json.dump(entries, f, indent=2, ensure_ascii=False)

    # Rebuild every dated page so navigation links stay correct.
    for i, entry in enumerate(entries):
        newer = entries[i - 1]["date"] if i > 0 else None
        older = entries[i + 1]["date"] if i + 1 < len(entries) else None
        page = _reflection_page(entry, prev_date=older, next_date=newer)
        with open(os.path.join(DOCS_DIR, f'{entry["date"]}.html'), "w",
                  encoding="utf-8") as f:
            f.write(page)

    # Homepage is a copy of the newest entry.
    newest = entries[0]
    home = _reflection_page(
        newest,
        prev_date=entries[1]["date"] if len(entries) > 1 else None,
        next_date=None,
    )
    with open(os.path.join(DOCS_DIR, "index.html"), "w", encoding="utf-8") as f:
        f.write(home)

    with open(os.path.join(DOCS_DIR, "archive.html"), "w", encoding="utf-8") as f:
        f.write(_archive_page(entries))

    with open(os.path.join(DOCS_DIR, "style.css"), "w", encoding="utf-8") as f:
        f.write(STYLE_CSS)

    with open(os.path.join(DOCS_DIR, "manifest.json"), "w", encoding="utf-8") as f:
        json.dump(MANIFEST, f, indent=2)

    # Stops GitHub Pages from running the files through Jekyll.
    open(os.path.join(DOCS_DIR, ".nojekyll"), "w").close()

    return f"{iso}.html"
